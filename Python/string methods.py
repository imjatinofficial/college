#string methods

name = 'jatin'
last_name = 'yadav'
print(name + last_name) #prints jatinyadav
print('ja' in name) #prints true it checks the string portion in the given string
print(name * 3) #multiplies amd prints the string
print(len(name))
print(name.capitalize()) #capitalize the first letter of the string
print(name.find('in',2,5)) #finds the value given from the starting range to the ending range in a string
print(name.isalnum()) #checks if the stirng only contains numbers and alphabets
print(name.isalpha()) #checks if the string only contains alphabets
print(name.isdigit()) #checks if the string only contains digits
